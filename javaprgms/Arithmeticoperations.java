class Arithmeticoperations
{
	public static void main(String args[])
	{
		int a=10,b=20,sum,sub,mul,div;
		sum=a+b;
		sub=a-b;
		mul=a*b;
		div=a/b;
		System.out.println("sum = "+sum);
		System.out.println("sub = "+sub);
		System.out.println("mul = "+mul);
		System.out.println("div = "+div);
	}
}