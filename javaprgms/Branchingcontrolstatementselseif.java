class Branchingcontrolstatementselseif
{
	public static void main(String args[])
	{
		int a=40;
		if(a<20)
		{
			System.out.println("The value is True");
		}
		else if(a==40)
		{
			System.out.println("The value is Equal");
		}
		else
		{
			System.out.println("The value is False");
		}
	}
}